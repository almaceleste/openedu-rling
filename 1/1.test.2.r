# Сколько различных 3-буквенных множеств можно составить, используя буквы
# русского алфавита?
# Множества {т, о, к} и {к, о, т} одинаковы.

abc <- c(
    'а','б','в','г','д','е','ё','ж','з','и','й',
    'к','л','м','н','о','п','р','с','т','у','ф',
    'х','ц','ч','ш','щ','ъ','ы','ь','э','ю','я'
    )
n <- length(abc)
k <- 3

combinations <-
    factorial(n) / (factorial(k) * factorial(n - k))

cat('combinations:', combinations)
